def get_theme(name):
    """Return a theme dict by name, raising a clear error if unknown."""
    if name not in THEMES:
        valid = ", ".join(sorted(THEMES.keys()))
        raise ValueError(f"Unknown theme '{name}'. Valid options: {valid}")
    return THEMES[name]
